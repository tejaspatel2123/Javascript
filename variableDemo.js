//three way to create variables in javascript

// 1. Using Var Keyword
// Syntax -- var <variable Name> = <values>
// Example -- var marks = 33;
var name = "peter";
var score = "4"; // this is the firs way to create js variable

// 2. Using Let Keyword
// Syntax -- let <variable Name> = <values>
// Example -- let marks = 33;
let age = 21;
let flag = false;

// 3. Using Const Keyword
// Syntax -- const <variable Name> = <values>
// Example -- const marks = 33;
const nameIs = "peter Parker";
